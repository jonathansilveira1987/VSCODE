exponenciacao = numero_1 ** numero_2

print('\nSoma:', soma)
print('Substração:', subtracao)
print('Multiplicação:', multiplicacao)
print('Divisão:', divisao)
print('Divisão Inteira:', divisao_inteira)
print('Módulo:', modulo)
